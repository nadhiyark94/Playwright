//Step 1: Create the Function printOddNumbers
function printOddNumbers() {
    for (let i = 1; i <= 25; i++) {      // Loop from 1 to 25
        if (i % 2 !== 0) {               // Check for odd number
            console.log(i);              // Print odd number
        }
    }
}
//Step 2: Call the Function from JavaScript
printOddNumbers();

